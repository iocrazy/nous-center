function ThumbStrip({ count }: { count: number }) {
  // Cheap visual: small bars indicating node count, capped at 5.
  const bars = Math.min(count, 5)
  return (
    <div
      style={{
        height: 60,
        background: 'var(--bg)',
        border: '1px solid var(--border)',
        borderRadius: 4,
        marginBottom: 10,
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        gap: 6,
        padding: 12,
      }}
    >
      {bars === 0 ? (
        <span style={{ color: 'var(--muted)', fontSize: 11 }}>空 workflow</span>
      ) : (
        Array.from({ length: bars }).map((_, i) => (
          <div
            key={i}
            style={{
              width: 36,
              height: 18,
              background: 'var(--bg-elevated, var(--bg-accent))',
              border: '1px solid var(--border)',
              borderRadius: 3,
              fontSize: 9,
              color: 'var(--muted)',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
            }}
          >
            n{i + 1}
          </div>
        ))
      )}
    </div>
  )
}

function Badge({
  color,
  children,
}: {
  color?: 'green' | 'purple'
  children: React.ReactNode
}) {
  const styles =
    color === 'green'
      ? { background: 'rgba(34,197,94,0.15)', color: 'var(--accent-2, #22c55e)' }
      : color === 'purple'
        ? { background: 'rgba(168,85,247,0.15)', color: 'var(--purple, #a855f7)' }
        : { background: 'var(--bg)', color: 'var(--muted)', border: '1px solid var(--border)' }
  return (
    <span style={{ fontSize: 10, padding: '1px 7px', borderRadius: 10, ...styles }}>
      {children}
    </span>
  )
}

// ---------- helpers ----------

function shortWfId(id: string): string {
  return id.length > 6 ? id.slice(-6) : id
}

function indexServicesByWorkflow(rows: ServiceRow[]): Map<string, ServiceRow> {
  const map = new Map<string, ServiceRow>()
  // Latest published version wins (services are sorted by created_at desc).
  for (const r of rows) {
    if (r.source_type !== 'workflow' || !r.workflow_id) continue
    const k = String(r.workflow_id)
    if (!map.has(k)) map.set(k, r)
  }
  return map
}

function buildCounts(rows: WorkflowSummary[]): Record<Tab, number> {
  return {
    all: rows.length,
    mine: rows.filter((r) => !r.is_template).length,
    template: rows.filter((r) => r.is_template).length,
    published: rows.filter((r) => r.status === 'published').length,
  }
}

function filter(rows: WorkflowSummary[], tab: Tab, search: string): WorkflowSummary[] {
  const q = search.trim().toLowerCase()
  return rows.filter((r) => {
    if (tab === 'mine' && r.is_template) return false
    if (tab === 'template' && !r.is_template) return false
    if (tab === 'published' && r.status !== 'published') return false
    if (q && !r.name.toLowerCase().includes(q)) return false
    return true
  })
}

// ---------- create + navigate ----------

function useCreateWorkflowAndGo() {
  const create = useCreateWorkflow()
  const navigate = useNavigate()
  return {
    isPending: create.isPending,
    mutate: () =>
      create.mutate(
        { name: '新工作流' },
        {
          onSuccess: (wf) => navigate(`/workflows/${wf.id}`),
        },
      ),
  }
}

